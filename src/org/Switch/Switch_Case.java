package org.Switch;

public class Switch_Case {
	
	public static void main(String[] args) {
		
		int i =2;
		
		switch (i) {
		
		case 1:
			
			System.out.println("Biriyani");
			break;

        case 2:
			
			System.out.println("Chocklate");
			break;
					
        case 3:
			
			System.out.println("Ice Cream");
			break;
			
        default:
        	
        	System.out.println("Invalid");

        	break;
		}
	}

}
