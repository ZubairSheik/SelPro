package org.Jumping;

public class Jumping_Statement {
	
	public static void main(String[] args) {
		
		for (int i = 0; i <=10; i++) {
			
			
			if (i==5) {
				
				continue;
				
				//break;
			}
			System.out.println(i);
			
		}
	}

	
}

// Continue will skip the particular iteration

// Beak will stop the loop once it get reached 5 level